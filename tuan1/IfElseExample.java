/*Print "Hello World" if x is greater than y.*/

public class IfElseExample {
    public static void main(String[] args) {
        int x = 50;
        int y = 30;

        if (x > y) {
            System.out.println("Hello World");
        } else {
            System.out.println("x is not greater than y");
        }
    }
}
